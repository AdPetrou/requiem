package Reqtificator.enums;

/**Enum of the known config file sections.
 * Denoted as [Category] in the config-file, these mark the section headers.
 */
public enum ConfigSections {
    /**Visual Merge (Races) category.
     * All mods listed in this category qualify as visual templates for
     * race records.
     */
    RaceVisuals,
    /**Visual Merge (NPCs) category.
     * All mods listed in this category qualify as visual templates for
     * NPC records.
     */
    NPCVisuals

}
